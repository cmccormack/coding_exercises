var leftMostColumnWithOne = function (binaryMatrix) {
  const [bottomRow, rightCol] = binaryMatrix.dimensions();

  let minCol = Infinity;
  for (let row = 0; row < bottomRow; row += 1) {
    let curCol = minCol === Infinity ? rightCol : minCol;
    for (let col = curCol - 1; col >= 0; col -= 1) {
      if (binaryMatrix.get(row, col)) {
        minCol = Math.min(minCol, col);
      } else {
        break;
      }
    }
  }
  return minCol === Infinity ? -1 : minCol;
};

